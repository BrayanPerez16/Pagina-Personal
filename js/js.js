$("#btn").click(function(){
    var progreso = 0;
    var intervalo = setInterval(function(){
        progreso = progreso + 5;
        $("#barra").css("width", progreso +
        "%").attr("aria-valuenow",
        progreso).text(progreso+ "%");
        if(progreso>=50){
            clearInterval(intervalo);
        }
    },100);
});
